#ifndef TYPE_DEFS_H_
#define TYPE_DEFS_H_

//1 byte data types
typedef unsigned char u8;
typedef signed char s8;
//2 byte data types
typedef short unsigned int u16;
typedef short signed int s16;
//4 byte data types
typedef unsigned int u32;
typedef int s32;
//floating data types
typedef float f32;
typedef double f64;
typedef long double fl128;

#define NULL  0               /* ((void* )0)*/

#endif /* TYPE_DEFS_H_ */
